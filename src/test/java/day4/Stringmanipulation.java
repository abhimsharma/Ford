package day4;

public class Stringmanipulation {

    public static void main(String args[]){

        String str = "Get well soon from corona";

        System.out.println(str);
        System.out.println(str.toUpperCase()); //prints whole string in uppercase
        System.out.println(str.length()); //length of the string
        System.out.println(str.toLowerCase()); //prints whole string in lowercase

    }
}
